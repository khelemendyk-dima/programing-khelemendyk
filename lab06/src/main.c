int main()
{
	// –∫–æ–Ω—Å—Ç–∞–Ω—Ç–∏, —è–∫—ñ —î —Ä–æ–∑–º—ñ—Ä–æ–º –º–∞—Å–∏–≤—É
#define A 2
#define B 3
	int matrix[A][B];
	// –∑–∞–ø–æ–≤–Ω—é—é –º–∞—Å–∏–≤ –≤—ñ–¥ 1 –¥–æ n
	for (int i = 0; i < A; i++) {
		for (int j = 0; j < B; j++) {
			int n = i * B + j + 1;
			matrix[i][j] = n;
		}
	}
	// –ø—Ä–∏—Å–≤–æ—é—é –∑–Ω–∞—á–µ–Ω–Ω—è 1 –µ–ª–µ–º–µ–Ω—Ç–∞ –º–∞—Å–∏–≤—É –≤ —Ç–∏–º—á–∞—Å–æ–≤—É –∑–º—ñ–Ω—É
	int tmp = matrix[0][0];
	// –≤–∏–∫–æ–Ω—É—é –∑—Å—É–≤ —Å–ø—Ä–∞–≤–∞ –Ω–∞–ª—ñ–≤–æ
	for (int n = 0; n < A; n++) {
		for (int p = 0; p < B; p++) {
			matrix[n][p] = matrix[n][p + 1];
		}
	}
	// –ø—Ä–∏—Å–≤–æ—é—é –æ—Å—Ç–∞–Ω–Ω—å–æ–º—É –µ–ª–µ–º–µ–Ω—Ç—É –∑–Ω–∞—á–µ–Ω–Ω—è tmp
	matrix[1][2] = tmp;
	return 0;
}
